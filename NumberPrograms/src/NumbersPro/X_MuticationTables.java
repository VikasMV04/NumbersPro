package NumbersPro;

public class X_MuticationTables {

	public static void main(String[] args) 
	{
		
			for (int j = 1; j <= 10; j++) 
			{
				for (int k = 1; k <= 10; k++) 
				{
					System.out.print(k + "x" + j + "=" + (k * j) + "  ");
				}
				System.out.println();
			}
		}
}
